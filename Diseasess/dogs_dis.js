function can_abt_fun() {
  var aboutt = document.getElementById("can_ab");
  var more_abtt = document.getElementById("can_more_abt");
  var disbox= document.getElementById("dis1");
  if (aboutt.style.display === "inline") {
    aboutt.style.display = "none";
    more_abtt.innerHTML = "ABOUT";
  } else {
    aboutt.style.display = "inline";
    more_abtt.innerHTML = "HIDE ABOUT";
  }
} 
