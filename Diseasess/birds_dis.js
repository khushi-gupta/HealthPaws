 function pd_abt_fun() {
    var abt = document.getElementById("pd_ab");
    var mabt = document.getElementById("pd_more_abt");
    if (abt.style.display === "inline") {
      abt.style.display = "none";
      mabt.innerHTML = "ABOUT"; 
    } else {
      abt.style.display = "inline";
      mabt.innerHTML = "HIDE ABOUT";
    }
  }
